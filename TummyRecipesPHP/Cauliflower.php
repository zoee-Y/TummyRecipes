                            <h5>3. Add the cauliflower and vegetable stock and cook until the cauliflower is softened and enough liquid has evaporated to make the mixture creamy, about 10 minutes.</h5>
                            <h5>4. Stir in the Parmesan and let sit 5 minutes to thicken. Finish by stirring in the chives.</h5>
