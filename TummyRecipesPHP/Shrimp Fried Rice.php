                <br>
                <p style="font-size: 25px"><b><u>Instructions</u></b></p>
                <h5>1. In a small bowl, whisk together soy sauce, sesame oil, ginger powder and white pepper; set aside. </h5>
                <h5>2. Heat olive oil in a large skillet or wok over medium high heat. 
                    Add shrimp, and cook, stirring occasionally, until pink, about 2-3 minutes; 
                    season with salt and pepper, to taste; set aside.</h5>
                <h5>3. Add garlic and onion to the skillet, and cook, stirring often, until onions have become translucent, 
                    about 3-4 minutes. Stir in carrots, corn and peas, and cook, stirring constantly, 
                    until vegetables are tender, about 3-4 minutes.</h5>
                <h5>4. Stir in rice, green onions and soy sauce mixture. 
                    Cook, stirring constantly, until heated through, about 2 minutes. Stir in shrimp.</h5>
                <h5>5. Serve immediately.</h5>
