                <h5>- 2 tablespoons olive oil</h5>
                <h5>- 1 pound medium shrimp, peeled and deveined</h5>
                <h5>- Kosher salt and freshly ground black pepper </h5>
                <h5>- 2 cloves garlic, minced</h5>
                <h5>- 1 onion, diced</h5>
                <h5>- 2 carrots, peeled and grated</h5>
                <h5>- 1/2 cup frozen corn</h5>
