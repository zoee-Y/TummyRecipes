                        <h3><b><u>Ingredients</u></b></h3>
                        <h5>- 1 1/2 cups jasmine rice </h5>
                        <h5>- 1 cup kimchi, coarsely chopped and 2 tablespoons juices reserved</h5>
                        <h5>- 3 tablespoons soy sauce</h5>
                        <h5>- 1 tablespoon gochujang (Korean red pepper paste)</h5>
                        <h5>- 1 tablespoon sesame oil</h5>
                        <h5>- 2 tablespoons canola oil</h5>
                        <h5>- 2 cloves garlic, minced</h5>
                        <h5>- 1 small onion, diced</h5>
                        <h5>- 2 teaspoons freshly grated ginger</h5>
                        <h5>- 1 (3.5-ounce) package shiitake mushrooms, sliced</h5>
                        <h5>- 1 cup matchstick carrots</h5>
                        <h5>- 1/2 bunch kale, stems removed and leaves chopped</h5>
                        <h5>- 4 fried eggs</h5>
                        <h5>- 1/4 cup nori strips</h5>
                        <h5>- 2 green onions, thinly sliced</h5>
                        <h5>- 1 tablespoon black sesame seeds</h5>
                        
                        <h3><b><u>Instructions</u></b></h3>
                        <h5>1. In a large saucepan of 3 cups water, cook rice according to package instructions; set aside.</h5>
                        <h5>2. In a small bowl, whisk together kimchi juice, soy sauce, gochujang and sesame oil.</h5>
                        <h5>3. Heat canola oil in a large skillet over medium high heat. Add garlic, onion and ginger, and cook, stirring frequently, until onions have become translucent, about 2-3 minutes.</h5>
                        <h5>4. Stir in kimchi, mushrooms and carrots. Cook, stirring constantly, until mushrooms are tender, about 3-4 minutes.</h5>
                        <h5>5. Stir in rice, kale and kimchi juice mixture until the kale has wilted, about 2-3 minutes.</h5>
                        <h5>6. Serve immediately with eggs, nori strips, green onions and sesame seeds, if desired.</h5>
                        
