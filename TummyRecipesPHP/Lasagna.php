                <h5>- 1 pound sweet Italian sausage</h5>
                <h5>- 1 pound lean ground beef</h5>
                <h5>- 1 large white onion diced</h5>
                <h5>- 5 cloves garlic minced</h5>
                <h5>- 1 (28 ounce) can crushed tomatoes</h5>
                <h5>- 2 (6 ounce) cans tomato paste</h5>
                <h5>- 1 (15 ounce) can tomato sauce</h5>
                <h5>- 1/2 cup white wine</h5>
                <h5>- 2 tablespoons white sugar</h5>
                <h5>- 1/2 cup chopped fresh basil</h5>
                <h5>- 1 teaspoon fennel seeds</h5>
                <h5>- 1 teaspoon ground oregano</h5>
                <h5>- 1/2 teaspoon salt</h5>
                <h5>- 1/4 teaspoon ground black pepper</h5>
                <h5>- 1/4 cup chopped fresh parsley</h5>
                <h5>- 1 pound lasagna noodles</h5>
                <h5>- 30 ounces ricotta cheese</h5>
                <h5>- 2 tablespoons chopped fresh parsley</h5>
                <h5>- 1 large egg</h5>
                <h5>- 1/2 teaspoon salt</h5>
                <h5>- 1/8 teaspoon ground nutmeg</h5>
                <h5>- 1 pound thinly sliced mozzarella cheese about 24 slices</h5>
                <h5>- 1 cup freshly grated Parmesan cheese</h5>
                <br>
                <br>
                <p style="font-size: 25px"><b><u>Instructions</u></b></p>
                <h5>1. In a large pot over medium heat, add in ground sausage and ground beef. Use a spoon to break up the meat into small pieces. Add in onion and garlic and cook until meat is well browned, stirring constantly. Stir in sugar, fresh basil, fennel, oregano, 1/2 teaspoon salt, pepper, and 1/4 cup chopped parsley. Pour in crushed tomatoes, tomato paste, tomato sauce, and wine*. Stir well and bring to a simmer. Reduce heat to low and simmer 1-4 hours, stirring occasionally.</h5>
                <h5>2. Meanwhile, place lasagna noodles into the bottom of a pan. Pour hot tap water directly over the noodles, making sure the pasta is completely immersed in the water. Let them soak for 30 minutes, then drain and discard water.</h5>
                <h5>3. In a mixing bowl, combine ricotta cheese with egg, remaining 2 tablespoons parsley, 1/2 teaspoon salt, and nutmeg. Refrigerate until ready to assemble lasagna.</h5>
                <h5>4. Preheat oven to 375 degrees. Lightly grease a pan.</h5>
                <h5>5. To assemble, spread about 1 cup of meat sauce in the bottom of the prepared pan. Place 4 noodles on top. Spread with 1/3 of the ricotta cheese mixture. Top with 1/4 of mozzarella cheese slices. Spoon 1 1/2 cups meat sauce over mozzarella, then sprinkle with 1/4 cup parmesan cheese. Repeat layering two more times to create three complete layers. To finish, place a final layer of pasta, topped with another 1 cup of meat sauce to cover the pasta. Top with remaining mozzarella and Parmesan cheese. Cover loosely with aluminum foil.</h5>
                <h5>6. Bake in preheated oven for 25 minutes. Remove foil, and bake an additional 25 minutes to allow cheese to brown. Serve hot.</h5>
